"""
Prepare synthetic pickleball dataset for MTL training.

Reads pickleball_shot_dataset.csv generated by generate_dataset.py,
applies StandardScaler, and saves 70/15/15 stratified splits to
./artifacts/splits.npz.

Run after generate_dataset.py:
    python generate_dataset.py
    python prepare_dataset.py
"""

import json
import os
import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler

RANDOM_SEED = 42
TRAIN_RATIO = 0.70
VAL_RATIO   = 0.15
TEST_RATIO  = 0.15

HERE = os.path.dirname(os.path.abspath(__file__))
DATA_CSV   = os.path.join(HERE, "data", "pickleball_shot_dataset.csv")
OUTPUT_DIR = os.path.join(HERE, "artifacts")

SHOT_TYPE_MAPPING = {
    "Drive":      0,
    "Drop":       1,
    "Dink":       2,
    "Lob":        3,
    "SpeedUp":    4,
    "HandBattle": 5,
}
CLASS_NAMES = ["Drive", "Drop", "Dink", "Lob", "SpeedUp", "HandBattle"]

INPUT_COLS     = ["x", "y", "z", "vx", "vy", "vz"]
REGRESSION_COLS = ["x_out", "y_out", "z_out", "vx_out", "vy_out", "vz_out"]
LABEL_COL      = "shot_type"


def load_csv(path: str) -> pd.DataFrame:
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"Dataset not found: {path}\n"
            "Run generate_dataset.py first."
        )
    df = pd.read_csv(path)
    print(f"Loaded {len(df):,} rows from {path}")
    return df


def main():
    print("=" * 60)
    print("Pickleball MTL Dataset Preparation (Synthetic)")
    print("=" * 60)

    df = load_csv(DATA_CSV)

    # Map string labels to integers
    df["shot_class"] = df[LABEL_COL].map(SHOT_TYPE_MAPPING)
    missing = df["shot_class"].isna().sum()
    if missing:
        print(f"WARNING: {missing} rows with unknown shot_type dropped.")
        df = df.dropna(subset=["shot_class"])
    df["shot_class"] = df["shot_class"].astype(np.int64)

    X   = df[INPUT_COLS].values.astype(np.float32)
    y_r = df[REGRESSION_COLS].values.astype(np.float32)
    y_c = df["shot_class"].values

    print(f"\nSamples: {len(X)}")
    print(f"Input features:      {X.shape[1]}  {INPUT_COLS}")
    print(f"Regression targets:  {y_r.shape[1]}  {REGRESSION_COLS}")
    print(f"Classes:             {len(CLASS_NAMES)}  {CLASS_NAMES}")

    # Class distribution
    print("\nClass distribution:")
    for i, name in enumerate(CLASS_NAMES):
        cnt = (y_c == i).sum()
        print(f"  {name:12s}  {cnt:6d}  ({100*cnt/len(y_c):.1f}%)")

    # 70 / 15 / 15 stratified split
    X_tv, X_test, yr_tv, yr_test, yc_tv, yc_test = train_test_split(
        X, y_r, y_c,
        test_size=TEST_RATIO,
        random_state=RANDOM_SEED,
        stratify=y_c,
    )

    val_frac = VAL_RATIO / (TRAIN_RATIO + VAL_RATIO)
    X_train, X_val, yr_train, yr_val, yc_train, yc_val = train_test_split(
        X_tv, yr_tv, yc_tv,
        test_size=val_frac,
        random_state=RANDOM_SEED,
        stratify=yc_tv,
    )

    print(f"\nSplit sizes — Train: {len(X_train):,}  Val: {len(X_val):,}  Test: {len(X_test):,}")

    # Scale
    input_scaler      = StandardScaler()
    regression_scaler = StandardScaler()

    X_train_s  = input_scaler.fit_transform(X_train)
    X_val_s    = input_scaler.transform(X_val)
    X_test_s   = input_scaler.transform(X_test)

    yr_train_s = regression_scaler.fit_transform(yr_train)
    yr_val_s   = regression_scaler.transform(yr_val)
    yr_test_s  = regression_scaler.transform(yr_test)

    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # Save splits
    splits_path = os.path.join(OUTPUT_DIR, "splits.npz")
    np.savez(
        splits_path,
        X_train=X_train_s.astype(np.float32),
        X_val=X_val_s.astype(np.float32),
        X_test=X_test_s.astype(np.float32),
        y_reg_train=yr_train_s.astype(np.float32),
        y_reg_val=yr_val_s.astype(np.float32),
        y_reg_test=yr_test_s.astype(np.float32),
        y_cls_train=yc_train,
        y_cls_val=yc_val,
        y_cls_test=yc_test,
    )

    # Save scaler params
    scaler_params = {
        "input_scaler": {
            "mean":  input_scaler.mean_.tolist(),
            "scale": input_scaler.scale_.tolist(),
            "cols":  INPUT_COLS,
        },
        "regression_scaler": {
            "mean":  regression_scaler.mean_.tolist(),
            "scale": regression_scaler.scale_.tolist(),
            "cols":  REGRESSION_COLS,
        },
        "class_names": CLASS_NAMES,
        "shot_type_mapping": SHOT_TYPE_MAPPING,
        "split": {"train": TRAIN_RATIO, "val": VAL_RATIO, "test": TEST_RATIO},
    }
    scaler_path = os.path.join(OUTPUT_DIR, "scaler_params.json")
    with open(scaler_path, "w") as f:
        json.dump(scaler_params, f, indent=2)

    print(f"\nSaved:")
    print(f"  {splits_path}")
    print(f"  {scaler_path}")
    print("\nDone.")


if __name__ == "__main__":
    main()
